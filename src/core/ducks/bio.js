
//////////////////////////////////////////////////////////
// State
//////////////////////////////////////////////////////////

const defaultState = {
  name: {
    prefix: "",
    first: "",
    middle: "",
    last: "",
    suffix: "",
    nickname: "",
  },
  text: [],
  images: [],
};


//////////////////////////////////////////////////////////
// Saga Actions
//////////////////////////////////////////////////////////

export const GET_PORTFOLIO = 'portfolio/sagas/GET_PORTFOLIO';

//////////////////////////////////////////////////////////
// Saga Action Creators
//////////////////////////////////////////////////////////

export const get_portfolio = (payload = {}) => {
  return {
    type: GET_PORTFOLIO,
    payload,
  };
}

//////////////////////////////////////////////////////////
// Reducer Actions
//////////////////////////////////////////////////////////

const UPDATE = 'bio/reducers/UPDATE';

//////////////////////////////////////////////////////////
// Reducer Action Creators
//////////////////////////////////////////////////////////

export const update = (payload) => {
  return {
    type: UPDATE,
    payload,
  };
}

//////////////////////////////////////////////////////////
// Reducer
//////////////////////////////////////////////////////////

const reducer = (state = defaultState, action) => {
  switch(action.type){
    case UPDATE:
      return {
        ...state,
        ...action.payload,
        name: {...state.name, ...action.payload.name}
      };
    default:
      return state;
  }
};

export default reducer;